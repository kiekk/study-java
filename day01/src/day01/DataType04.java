package day01;
/*
 	자바의 기본자료형
 	- 변수 : 저장소
 	- 변수 선언 : 타입 변수명
 	- (c언어) 	 : char short int long float double
 	- (java언어) : ★boolean char byte short int long float double
 	
 	short : c, c++에서 사용한 자료형과 호환 하기 위해서 남겨둔 자료형
 		
*/
public class DataType04 {

	public static void main(String[] args) {
		short a = 32767;
		short b = -32768;
		System.out.println("short 변수 a : " + a);
		System.out.println("short 변수 b : " + b);
	}

}
